// Bundles all data into web/data.js (multi-class) so the planner runs from file://.
// Usage: node scripts/bundle-data.mjs
import { readFileSync, writeFileSync, readdirSync } from "node:fs";

const read = (f) => JSON.parse(readFileSync(`data/${f}`, "utf8"));
const CLASSES = ["technomancer", "pyromancer", "trickster", "devastator"];

// Map each skill to its Breadbuilder icon file by normalized-name match.
const norm = (s) => s.toLowerCase().replace(/[^a-z0-9]/g, "");
const iconFiles = readdirSync("assets/skills").filter((f) => /\.(png|webp)$/.test(f));
const iconByNorm = Object.fromEntries(iconFiles.map((f) => [norm(f.replace(/\.(png|webp)$/, "")), f]));

// PAX node icons (GamerGuides), mapped by normalized node name.
let paxIcons = {};
try { paxIcons = JSON.parse(readFileSync("data/pax-icons.json", "utf8")); } catch {}
// PAX node positions (manually mapped over the tree images), per class/branch/name.
let paxCoords = {};
try { paxCoords = JSON.parse(readFileSync("data/pax-coords.json", "utf8")); } catch {}

const classes = {};
for (const cls of CLASSES) {
  const skills = read(`classes/${cls}.skills.json`);
  for (const s of skills.skills) s.icon = iconByNorm[norm(s.name)] || null;
  const pax = read(`classes/${cls}.pax.json`);
  const coords = paxCoords[cls] || {};
  for (const b of pax.branches) for (const n of b.nodes) {
    n.icon = paxIcons[norm(n.name)] || null;
    const xy = coords[b.name]?.[n.name];
    if (xy) { n.x = xy[0]; n.y = xy[1]; }
  }
  classes[cls] = {
    skilltree: read(`classes/${cls}.skilltree.json`),
    skills,
    pax,
    armor: read(`classes/${cls}.armor.json`),
  };
}

let weaponFirepower = {};
try { weaponFirepower = read("weapon-firepower.json"); } catch {}
let armorUpgrade = null;
try { armorUpgrade = read("armor-upgrade.json"); } catch {}

const DATA = {
  weapons: read("legendary-weapons.json"),  // universal
  mods: read("mods.json"),                   // universal weapon/armor mods (+ class skill mods)
  ascension: read("ascension.json"),         // universal
  weaponFirepower,                            // per-type firepower model (outriders.app)
  armorUpgrade,                               // armor defense model (outriders.app)
  classes,
};

writeFileSync(
  "data.js",
  `// AUTO-GENERATED by scripts/bundle-data.mjs — do not edit by hand.\nwindow.OUTRIDERS_DATA = ${JSON.stringify(DATA)};\n`
);

console.error("Bundled data.js");
console.error(`  weapons: ${DATA.weapons.length}, mods: ${DATA.mods.length}, ascension cats: ${DATA.ascension.categories.length}`);
for (const cls of CLASSES) {
  const c = classes[cls];
  console.error(`  ${cls}: tree ${c.skilltree.nodes.length}, skills ${c.skills.skills.length}, pax ${c.pax.branches.reduce((a, b) => a + b.nodes.length, 0)}, armor ${c.armor.length}`);
}
