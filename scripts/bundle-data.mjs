// Bundles all data into web/data.js (multi-class) so the planner runs from file://.
// Usage: node scripts/bundle-data.mjs
import { readFileSync, writeFileSync, mkdirSync } from "node:fs";

const read = (f) => JSON.parse(readFileSync(`data/${f}`, "utf8"));
const CLASSES = ["technomancer", "pyromancer", "trickster", "devastator"];

const classes = {};
for (const cls of CLASSES) {
  classes[cls] = {
    skilltree: read(`classes/${cls}.skilltree.json`),
    skills: read(`classes/${cls}.skills.json`),
    pax: read(`classes/${cls}.pax.json`),
    armor: read(`classes/${cls}.armor.json`),
  };
}

const DATA = {
  weapons: read("legendary-weapons.json"),  // universal
  mods: read("mods.json"),                   // universal weapon/armor mods (+ class skill mods)
  ascension: read("ascension.json"),         // universal
  classes,
};

writeFileSync(
  "data.js",
  `// AUTO-GENERATED by scripts/bundle-data.mjs — do not edit by hand.\nwindow.OUTRIDERS_DATA = ${JSON.stringify(DATA)};\n`
);

console.error("Bundled data.js");
console.error(`  weapons: ${DATA.weapons.length}, mods: ${DATA.mods.length}, ascension cats: ${DATA.ascension.categories.length}`);
for (const cls of CLASSES) {
  const c = classes[cls];
  console.error(`  ${cls}: tree ${c.skilltree.nodes.length}, skills ${c.skills.skills.length}, pax ${c.pax.branches.reduce((a, b) => a + b.nodes.length, 0)}, armor ${c.armor.length}`);
}
